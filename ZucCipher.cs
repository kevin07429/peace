    // Global state (matching C++ globals exactly)
    private static readonly uint[] g_LFSR_SX = new uint[16];
    private static uint g_F_R1, g_F_R2;
    private static readonly uint[] g_BRC_XX = new uint[4];

    private static uint Rot(uint a, int k) => (a << k) | (a >> (32 - k));
    private static uint AddM(uint a, uint b) { uint c = a + b; return (c & 0x7FFFFFFF) + (c >> 31); }
    private static uint MulByPow2(uint x, int k) => ((x << k) | (x >> (31 - k))) & 0x7FFFFFFF;
    private static uint MakeU32(byte a, byte b, byte c, byte d) => ((uint)a << 24) | ((uint)b << 16) | ((uint)c << 8) | d;
    private static uint MakeU31(byte a, ushort d, byte b) => ((uint)a << 23) | ((uint)d << 8) | b;

    private static uint L1(uint X) => X ^ Rot(X, 2) ^ Rot(X, 10) ^ Rot(X, 18) ^ Rot(X, 24);
    private static uint L2(uint X) => X ^ Rot(X, 8) ^ Rot(X, 14) ^ Rot(X, 22) ^ Rot(X, 30);

    private static void BitReorganization()
    {
        g_BRC_XX[0] = ((g_LFSR_SX[15] & 0x7FFF8000) << 1) | (g_LFSR_SX[14] & 0xFFFF);
        g_BRC_XX[1] = ((g_LFSR_SX[11] & 0xFFFF) << 16) | (g_LFSR_SX[9] >> 15);
        g_BRC_XX[2] = ((g_LFSR_SX[7] & 0xFFFF) << 16) | (g_LFSR_SX[5] >> 15);
        g_BRC_XX[3] = ((g_LFSR_SX[2] & 0xFFFF) << 16) | (g_LFSR_SX[0] >> 15);
    }

    private static uint F()
    {
        uint W = (g_BRC_XX[0] ^ g_F_R1) + g_F_R2;
        uint W1 = g_F_R1 + g_BRC_XX[1];
        uint W2 = g_F_R2 ^ g_BRC_XX[2];
        uint u = L1((W1 << 16) | (W2 >> 16));
        uint v = L2((W2 << 16) | (W1 >> 16));
        g_F_R1 = MakeU32(S0[u >> 24], S1[(u >> 16) & 0xFF], S0[(u >> 8) & 0xFF], S1[u & 0xFF]);
        g_F_R2 = MakeU32(S0[v >> 24], S1[(v >> 16) & 0xFF], S0[(v >> 8) & 0xFF], S1[v & 0xFF]);
        return W;
    }

    private static void LFSRWithInitializationMode(uint u)
    {
        uint f = g_LFSR_SX[0];
        f = AddM(f, MulByPow2(g_LFSR_SX[0], 8));
        f = AddM(f, MulByPow2(g_LFSR_SX[4], 20));
        f = AddM(f, MulByPow2(g_LFSR_SX[10], 21));
        f = AddM(f, MulByPow2(g_LFSR_SX[13], 17));
        f = AddM(f, MulByPow2(g_LFSR_SX[15], 15));
        f = AddM(f, u);
        for (int i = 0; i < 15; i++) g_LFSR_SX[i] = g_LFSR_SX[i + 1];
        g_LFSR_SX[15] = f;
    }

    private static void LFSRWithWorkMode()
    {
        uint f = g_LFSR_SX[0];
        f = AddM(f, MulByPow2(g_LFSR_SX[0], 8));
        f = AddM(f, MulByPow2(g_LFSR_SX[4], 20));
        f = AddM(f, MulByPow2(g_LFSR_SX[10], 21));
        f = AddM(f, MulByPow2(g_LFSR_SX[13], 17));
        f = AddM(f, MulByPow2(g_LFSR_SX[15], 15));
        for (int i = 0; i < 15; i++) g_LFSR_SX[i] = g_LFSR_SX[i + 1];
        g_LFSR_SX[15] = f;
    }

    private static void ExpandKey(byte[] key, byte[] iv)
    {
        for (int i = 0; i < 16; i++)
            g_LFSR_SX[i] = MakeU31(key[i], EK_d[i], iv[i]);
    }

    private static void Init(byte[] key, byte[] iv)
    {
        g_F_R1 = g_F_R2 = 0;
        ExpandKey(key, iv);
        for (int n = 0; n < 32; n++)
        {
            BitReorganization();
            uint w = F();
            LFSRWithInitializationMode(w >> 1);
        }
    }

    /// Generate key array matching PRPakTool ZUC::GenerateKeyArray exactly
    public static uint[] GenerateKeyArray(byte[] key, byte[] iv, int count)
    {
        if (key.Length != 16 || iv.Length != 16 || count <= 0)
            throw new ArgumentException("Invalid ZUC parameters");
        Init(key, iv);

        // Discard round
        BitReorganization();
        F();
        LFSRWithWorkMode();

        var keys = new uint[count];
        for (uint i = 0; i < count; i++)
        {
            BitReorganization();
            keys[i] = F() ^ g_BRC_XX[3];
            LFSRWithWorkMode();
        }

        // Clean state
        g_F_R1 = g_F_R2 = 0;
        for (int i = 0; i < 16; i++) g_LFSR_SX[i] = 0;
        for (int i = 0; i < 4; i++) g_BRC_XX[i] = 0;
        return keys;
    }

    public static byte[] GenerateKeyStream(byte[] key, byte[] iv, int byteCount)
    {
        var keys = GenerateKeyArray(key, iv, (byteCount + 3) / 4);
        var stream = new byte[byteCount];
        for (int i = 0; i < byteCount; i++)
            stream[i] = (byte)(keys[i / 4] >> (24 - (i % 4) * 8));
        return stream;
    }
